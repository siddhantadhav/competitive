#include <bits/stdc++.h>
using namespace std;

int main() {
	clock_t tStart = clock();
	freopen("../../input.txt", "r", stdin);
	freopen("../../output.txt", "w", stdout);

	cout << "Hello World" << endl;

	fprintf(stderr, "\n>> Runtime: %.10fs\n", (double) (clock() - tStart) / CLOCKS_PER_SEC);

	return 0;
}